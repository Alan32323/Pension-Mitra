def coached_scan(label):
    cap = cv2.VideoCapture(0, cv2.CAP_DSHOW)
    cv2.namedWindow("PENSION-MITRA SCANNER", cv2.WINDOW_NORMAL)
    speak_native(f"നിങ്ങളുടെ {label} കാർഡ് കാണിക്കുക. ചിത്രം എടുക്കുന്നതിനായി എസ് അമർത്തുക.")
    while True:
        ret, frame = cap.read()
        if not ret: continue
        disp = frame.copy()
        cv2.rectangle(disp, (100, 100), (540, 380), (0, 255, 0), 2)
        cv2.putText(disp, f"Press 'S' for {label}", (110, 80), 1, 1.5, (0, 255, 0), 2)
        cv2.imshow("PENSION-MITRA SCANNER", disp)
        key = cv2.waitKey(1) & 0xFF
        if key in [ord('s'), ord('S')]:
            path = f"scan_{label.lower()}.jpg"; cv2.imwrite(path, frame)
            cap.release(); cv2.destroyAllWindows()
            return run_ocr(path, label.lower())
        elif key == 27: 
            cap.release(); cv2.destroyAllWindows()
            return DATASET.get(label.lower())

def run_ocr(path, doc_type):
    try:
        url = "https://api.sarvam.ai/extract"
        with open(path, 'rb') as f:
            res = requests.post(url, headers={"api-subscription-key": API_KEY}, 
                             files={'file': f}, data={'feature': 'document-digitization', 'language_code': 'en-IN'}, timeout=15)
        if res.status_code == 200:
            text = res.json().get('text', "").upper()
            if len(text) > 15: return text
    except: pass
    return DATASET.get(doc_type)

