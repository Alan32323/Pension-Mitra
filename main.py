# --- 6. MAIN CONTROLLER ---
async def main_async():
    if not API_KEY: print("API Key Missing!"); return
    u_id, u_name = user_session_manager()
    
    # 1. Capture Documents
    aad_text = coached_scan("Aadhaar")
    pen_text = coached_scan("Pension")

    # 2. Extract Data
    def get_val(pattern, text, default):
        match = re.search(pattern, text)
        return match.group(1).strip() if match else default

    # Extract Name, DOB, and Account Number
    a_name = get_val(r"NAME:\s*(.*?)(?=DOB|$)", aad_text, u_name).upper()
    p_name = get_val(r"NAME:\s*(.*?)(?=DOB|$)", pen_text, "RAGHAVAN MK").upper()
    
    a_dob = get_val(r"DOB:\s*(\d{2}/\d{2}/\d{4})", aad_text, "01/02/1960")
    p_dob = get_val(r"DOB:\s*(\d{2}/\d{2}/\d{4})", pen_text, "01/02/1960")

    # --- NEW: Extract Account Numbers ---
    a_acc = get_val(r"ACC:\s*(\d+)", aad_text, "00000000")
    p_acc = get_val(r"ACC:\s*(\d+)", pen_text, "11111111") # Example mismatch

    # 3. Detect Mismatches
    mismatches = []
    
    # Name Check
    if fuzz.ratio(a_name, p_name) < 90:
        mismatches.append(f"NAME (Aadhaar: {a_name} | Pension: {p_name})")
    
    # DOB Check
    if a_dob != p_dob:
        mismatches.append(f"DOB (Aadhaar: {a_dob} | Pension: {p_dob})")
    
    # --- NEW: Account Number Check ---
    if a_acc != p_acc:
        mismatches.append(f"BANK ACCOUNT (Aadhaar: {a_acc} | Pension: {p_acc})")

    # 4. Routing
    if not mismatches:
        print("✅ All data matches. Proceeding to Life Certificate...")
        await interactive_guide_automated(u_name, "88776655", a_dob)
    else:
        # If any of the 3 (Name, DOB, or ACC) fail, show the letter
        speak_native("ക്ഷമിക്കണം, രേഖകളിൽ ചില പൊരുത്തക്കേടുകൾ ഉണ്ട്. തിരുത്തൽ കത്ത് തയ്യാറാക്കിയിട്ടുണ്ട്.")
        show_correction_letter(u_name, "88776655", mismatches)

if __name__ == "__main__":
    asyncio.run(main_async())