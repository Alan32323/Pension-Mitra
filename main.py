# --- 2. VOICE ENGINES ---

def speak_native(text):
    if not text: return
    print(f"[VOICE]: {text}")
    url = "https://api.sarvam.ai/text-to-speech"
    headers = {"api-subscription-key": API_KEY, "Content-Type": "application/json"}
    payload = {"text": text, "target_language_code": "ml-IN", "speaker": "ritu", "model": "bulbul:v3"}
    try:
        res = requests.post(url, json=payload, headers=headers, timeout=8)
        if res.status_code == 200:
            with open("voice.wav", "wb") as f:
                f.write(base64.b64decode(res.json()['audios'][0]))
            winsound.PlaySound("voice.wav", winsound.SND_FILENAME)
    except Exception as e:
        print(f"TTS Error: {e}")

def listen_for_command():
    recognizer = sr.Recognizer()
    with sr.Microphone() as source:
        print("\n[LISTENING] (അടുത്തത് / വീണ്ടും / നിർത്താം)...")
        recognizer.adjust_for_ambient_noise(source, duration=0.8)
        try:
            audio = recognizer.listen(source, timeout=7)
            text = recognizer.recognize_google(audio, language='ml-IN').lower()
            print(f">> User said: {text}")
            return text
        except:
            return ""

